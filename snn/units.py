pA = ampere / 1e12

farad = 1e6
mF = farad / 1e3
uF = farad / 1e6
nF = farad / 1e9
pF = farad / 1e12

siemens = 1e3
mS = siemens / 1e3
uS = siemens / 1e6
nS = siemens / 1e9
pS = siemens / 1e12

O = voltage / ampere

litre = 1e3
mL = litre / 1e3

mol = 6.02214076e23
molar = mol / litre
